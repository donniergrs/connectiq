export function findCarrierProfile(providerName = "") {
  const normalized = providerName.toLowerCase();

  return carrierProfiles.find((carrier) =>
    carrier.aliases.some((alias) => normalized.includes(alias.toLowerCase())) ||
    normalized.includes(carrier.brand.toLowerCase())
  );
}
